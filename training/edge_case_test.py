def main():
    tokenizer, model, device = load_model()
    categories = run_tests(tokenizer, model, device)

    results = {}
    total_tests = 0
    total_correct = 0
    total_missed = 0

    for cat_name, test_cases in categories.items():
        correct = 0
        missed = []
        for text, expected in test_cases:
            pred, conf = predict(text, tokenizer, model, device)
            total_tests += 1
            if pred == expected:
                correct += 1
                total_correct += 1
            else:
                total_missed += 1
                missed.append({"text": text, "expected": expected, "predicted": pred, "confidence": round(conf, 4)})

        accuracy = correct / len(test_cases) * 100
        results[cat_name] = {
            "total": len(test_cases),
            "correct": correct,
            "accuracy": round(accuracy, 1),
            "missed": missed,
        }

        status = "✅" if accuracy >= 80 else "⚠️" if accuracy >= 50 else "❌"
        print(f"{status} {cat_name:30s} {correct:>3}/{len(test_cases):>3} ({accuracy:.0f}%)")
        for m in missed:
            exp = "TOXIC" if m["expected"] == 1 else "SAFE"
            got = "TOXIC" if m["predicted"] == 1 else "SAFE"
            print(f"     MISS: \"{m['text'][:50]}\" expected={exp} got={got} conf={m['confidence']:.1%}")

    print(f"\n{'='*60}")
    print(f"TOTAL: {total_correct}/{total_tests} ({total_correct/total_tests*100:.1f}%)")
    print(f"MISSED: {total_missed}")

    # Save report
    with open(REPORT_PATH, "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)
    print(f"\nReport saved: {REPORT_PATH}")

if __name__ == "__main__":
    main()
